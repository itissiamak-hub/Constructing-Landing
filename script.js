/* ----- Scroll-linked background photos ----- */
var formSubmitted = false;
var bgConstruction = document.getElementById('bgConstruction');
var bgProgress = document.getElementById('bgProgress');
var bgCompleted = document.getElementById('bgCompleted');

function updateBackground() {
  if (!bgConstruction || !bgProgress || !bgCompleted) return;

  if (formSubmitted) {
    bgConstruction.style.opacity = '0';
    bgProgress.style.opacity = '0';
    bgCompleted.style.opacity = '1';
    return;
  }

  var scrollY = window.scrollY || window.pageYOffset;
  var docHeight = document.documentElement.scrollHeight - window.innerHeight;
  var progress = docHeight <= 0 ? 0 : Math.min(1, Math.max(0, scrollY / docHeight));

  bgConstruction.style.opacity = String(1 - progress);
  bgProgress.style.opacity = String(progress);
  bgCompleted.style.opacity = '0';
}

function onScroll() {
  requestAnimationFrame(updateBackground);
}

window.addEventListener('scroll', onScroll, { passive: true });
window.addEventListener('resize', onScroll);
updateBackground();

(function removeInvalidOnInput() {
  var form = document.getElementById('requestForm');
  if (!form) return;
  var fields = [form.fullname, form.company, form.phoneCountry, form.phoneNumber, form.email, form.inquiry];
  fields.forEach(function (el) {
    if (!el) return;
    el.addEventListener('input', function () { el.classList.remove('is-invalid'); });
    el.addEventListener('change', function () { el.classList.remove('is-invalid'); });
    el.addEventListener('blur', function () {
      var v = el.value;
      if (v && typeof v.trim === 'function') v = v.trim();
      if (v) el.classList.remove('is-invalid');
    });
  });
})();
