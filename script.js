let currentLang = 'en';

function applyLanguage(lang) {
  currentLang = lang;
  const t = translations[lang];
  if (!t) return;

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (key && t[key]) el.textContent = t[key];
  });

  // On small screens, use a simple heading without per-letter animation
  const heroEl = document.getElementById('heroTitle');
  const heroText = t['hero.title'];
  if (heroEl && heroText) {
    if (window.innerWidth <= 640) {
      heroEl.textContent = heroText.replace(/\s*\n\s*/g, ' ');
    } else {
      animateHeroLetters();
    }
  } else {
    animateHeroLetters();
  }

  const placeholders = t.placeholder;
  if (placeholders) {
    const fullname = document.getElementById('fullname');
    const company = document.getElementById('company');
    const phoneNumber = document.getElementById('phoneNumber');
    const email = document.getElementById('email');
    const inquiry = document.getElementById('inquiry');
    if (fullname) fullname.placeholder = placeholders.fullname;
    if (company) company.placeholder = placeholders.company;
    if (phoneNumber) phoneNumber.placeholder = placeholders.phone;
    if (email) email.placeholder = placeholders.email;
    if (inquiry) inquiry.placeholder = placeholders.inquiry;
  }

  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.getAttribute('data-lang') === lang);
  });

  document.body.classList.remove('lang-en', 'lang-de', 'lang-fa', 'rtl');
  if (lang === 'fa') {
    document.documentElement.setAttribute('lang', 'fa');
    document.body.classList.add('rtl', 'lang-fa');
  } else {
    document.documentElement.setAttribute('lang', lang === 'de' ? 'de' : 'en');
    document.body.classList.add(lang === 'de' ? 'lang-de' : 'lang-en');
  }

  var modalTitle = document.getElementById('modalTitle');
  var modalMessage = document.getElementById('modalMessage');
  var modalClose = document.getElementById('modalClose');
  if (t['modal.title'] && modalTitle) modalTitle.textContent = t['modal.title'];
  if (t['modal.message'] && modalMessage) modalMessage.textContent = t['modal.message'];
  if (t['modal.close'] && modalClose) modalClose.textContent = t['modal.close'];
}

function animateHeroLetters() {
  var el = document.getElementById('heroTitle');
  if (!el) return;
  var text = translations[currentLang] && translations[currentLang]['hero.title'];
  if (!text) return;

  if (currentLang === 'fa') {
    el.innerHTML = text.replace(/\n/g, '<br>');
    return;
  }

  el.innerHTML = '';
  var delayIndex = 0;
  var line2Wrap = null;
  for (var i = 0; i < text.length; i++) {
    if (text[i] === '\n') {
      el.appendChild(document.createElement('br'));
      line2Wrap = document.createElement('span');
      line2Wrap.className = 'hero-line-2' + (currentLang === 'de' ? ' hero-line-2-de' : '');
      el.appendChild(line2Wrap);
      continue;
    }
    var span = document.createElement('span');
    span.className = 'hero-letter' + (text[i] === ' ' ? ' hero-letter-space' : '');
    span.style.animationDelay = (delayIndex * 0.032) + 's';
    span.textContent = text[i];
    if (line2Wrap) {
      line2Wrap.appendChild(span);
    } else {
      el.appendChild(span);
    }
    delayIndex++;
  }
}

document.querySelectorAll('.lang-btn').forEach(btn => {
  btn.addEventListener('click', () => {
    const lang = btn.getAttribute('data-lang');
    applyLanguage(lang);
  });
});

var thankYouModal = document.getElementById('thankYouModal');
var modalCloseBtn = document.getElementById('modalClose');

function openThankYouModal() {
  if (thankYouModal) {
    thankYouModal.removeAttribute('hidden');
    thankYouModal.classList.add('is-open');
    modalCloseBtn && modalCloseBtn.focus();
    document.body.style.overflow = 'hidden';
  }
}

function closeThankYouModal() {
  if (thankYouModal) {
    thankYouModal.classList.remove('is-open');
    thankYouModal.setAttribute('hidden', '');
    document.body.style.overflow = '';
  }
  formSubmitted = false;
  updateBackground();
}

if (modalCloseBtn) {
  modalCloseBtn.addEventListener('click', closeThankYouModal);
}

if (thankYouModal) {
  thankYouModal.addEventListener('click', function (e) {
    if (e.target === thankYouModal) closeThankYouModal();
  });
  document.addEventListener('keydown', function (e) {
    if (e.key === 'Escape' && thankYouModal.classList.contains('is-open')) closeThankYouModal();
  });
}

