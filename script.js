function isValidPhoneNumber(countryCode, digits) {
  if (!countryCode || !digits) return false;
  return /^\d+$/.test(digits) && digits.length === 10;
}

function isValidFullname(s) {
  if (!s.length) return false;
  return /^[a-zA-Z\u00C0-\u024F\u0400-\u04FF\u0600-\u06FF\s\-']+$/.test(s);
}

function isValidEmail(s) {
  return /^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(s) && s.length <= 254;
}

document.getElementById('requestForm').addEventListener('submit', function (e) {
  e.preventDefault();
  var form = this;

  var fullname = form.fullname.value.trim();
  var company = form.company.value.trim();
  var countryCode = (form.phoneCountry && form.phoneCountry.value) || '';
  var phoneDigits = (form.phoneNumber && form.phoneNumber.value.replace(/\D/g, '')) || '';
  var email = form.email.value.trim();
  var inquiry = form.inquiry.value.trim();
  var phone = countryCode && phoneDigits ? countryCode + ' ' + phoneDigits : '';

  var required = [
    { value: fullname, el: form.fullname },
    { value: company, el: form.company },
    { value: countryCode, el: form.phoneCountry },
    { value: phoneDigits, el: form.phoneNumber },
    { value: email, el: form.email },
    { value: inquiry, el: form.inquiry }
  ];

  var firstInvalid = null;
  for (var i = 0; i < required.length; i++) {
    if (!required[i].value) {
      required[i].el.classList.add('is-invalid');
      if (!firstInvalid) firstInvalid = required[i].el;
    } else {
      required[i].el.classList.remove('is-invalid');
    }
  }

  var existingMsg = form.querySelector('.form-message');
  if (existingMsg) existingMsg.remove();

  if (firstInvalid) {
    showFormError(form, translations[currentLang]['form.error.required'] || 'Please fill in all required fields.');
    firstInvalid.focus();
    return;
  }

  if (!isValidFullname(fullname)) {
    form.fullname.classList.add('is-invalid');
    showFormError(form, translations[currentLang]['form.error.fullname'] || 'Full name may only contain letters, spaces, hyphens and apostrophes.');
    form.fullname.focus();
    return;
  }
  if (!isValidPhoneNumber(countryCode, phoneDigits)) {
    if (form.phoneCountry) form.phoneCountry.classList.add('is-invalid');
    if (form.phoneNumber) form.phoneNumber.classList.add('is-invalid');
    showFormError(form, translations[currentLang]['form.error.phone'] || 'Select a country and enter exactly 10 digits.');
    if (form.phoneNumber) form.phoneNumber.focus();
    return;
  }
  if (!isValidEmail(email)) {
    form.email.classList.add('is-invalid');
    showFormError(form, translations[currentLang]['form.error.email'] || 'Please enter a valid email address.');
    form.email.focus();
    return;
  }

  form.reset();
  applyLanguage(currentLang);
  formSubmitted = true;
  updateBackground();
  openThankYouModal();
});

function showFormError(form, text) {
  var existing = form.querySelector('.form-message');
  if (existing) existing.remove();
  var msg = document.createElement('p');
  msg.className = 'form-message error';
  msg.textContent = text;
  form.appendChild(msg);
}

